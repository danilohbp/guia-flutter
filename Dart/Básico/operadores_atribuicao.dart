void main(){
  // Atribuição
  var num1 = 10;
  
  // Atribução de Adição
  num1 += 1;
  print(num1);

  // Atribução de Subtração
  num1 -= 1;
  print(num1);

  // Atribução de Multiplicação
  num1 *= 1;
  print(num1);

  // Atribução de Divisão
  num1 ~/= 1;
  print(num1);

  // Atribução de Módulo
  num1 %= 1;
  print(num1);
}